#include <unistd.h>

void	ft_putchar(char c);
void	ft_putstr2(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int i;

	i = 1;
	while (i < argc)
	{
		ft_putstr2(argv[i]);
		ft_putchar('\n');
		i++;
	}
	return (0);
}
